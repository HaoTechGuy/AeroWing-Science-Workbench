export type CopyKey = keyof typeof COPY.zh;

export function isUiLanguage(value: unknown): value is UiLanguage {
  return value === "zh" || value === "en";
}

export function normalizeLanguage(value: unknown): UiLanguage {
  return isUiLanguage(value) ? value : "zh";
}

export function translate(
  language: UiLanguage,
  key: CopyKey,
  params?: Record<string, string | number>
): string {
  let value: string = COPY[language][key] || COPY.zh[key] || key;
  if (params) {
    for (const [paramKey, paramValue] of Object.entries(params)) {
      value = value.replaceAll(`{${paramKey}}`, String(paramValue));
    }
  }
  return value;
}

export function readStoredLanguage(): UiLanguage | null {
  if (typeof window === "undefined") return null;
  try {
    const value = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return isUiLanguage(value) ? value : null;
  } catch {
    return null;
  }
}

export function inferBrowserLanguage(): UiLanguage {
  if (typeof navigator === "undefined") return "zh";
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function writeStoredLanguage(language: UiLanguage): void {
  if (typeof window === "undefined") return;
  try {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    window.dispatchEvent(
      new CustomEvent(LANGUAGE_CHANGE_EVENT, { detail: { language } })
    );
  } catch {
    // Local storage can be disabled; the in-memory hook state still updates.
  }
}

export function displayResourceLabel(
  label: string | undefined,
  language: UiLanguage
): string {
  if (!label) return language === "zh" ? "当前机器" : "Current machine";
  if (label === "Current Machine" || label === "当前机器") {
    return language === "zh" ? "当前机器" : "Current machine";
  }
  return label;
}
