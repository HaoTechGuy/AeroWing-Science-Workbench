async function runCommandCandidates(
  commands: string[],
  args: string[],
  options?: {
    cwd?: string;
    env?: NodeJS.ProcessEnv;
    timeoutMs?: number;
  }
): Promise<{ command: string; stdout: string; stderr: string }> {
  if (commands.length === 0) {
    throw new Error("No configured command candidates.");
  }

  const errors: string[] = [];
  for (const command of commands) {
    try {
      const result = await execFileAsync(command, args, {
        cwd: options?.cwd,
        env: options?.env,
        timeout: options?.timeoutMs || OFFICE_PREPROCESS_TIMEOUT_MS,
        maxBuffer: OFFICE_PREPROCESS_MAX_BUFFER,
      });
      return {
        command,
        stdout: result.stdout,
        stderr: result.stderr,
      };
    } catch (error) {
      errors.push(formatCommandFailure(command, error));
    }
  }
  throw new Error(errors.join("\n"));
}

function formatCommandFailure(command: string, error: unknown): string {
  const record = error as {
    code?: unknown;
    signal?: unknown;
    message?: unknown;
    stderr?: unknown;
    stdout?: unknown;
  };
  const detail =
    typeof record.stderr === "string" && record.stderr.trim()
      ? record.stderr.trim()
      : typeof record.stdout === "string" && record.stdout.trim()
        ? record.stdout.trim()
        : typeof record.message === "string"
          ? record.message
          : String(error);
  const status = [record.code, record.signal].filter(Boolean).join("/");
  return `${command}${status ? ` (${status})` : ""}: ${truncateText(detail, 600)}`;
}

function formatErrorMessage(error: unknown): string {
  return error instanceof Error ? error.message : String(error);
}

function truncateText(value: string, maxLength: number): string {
  return value.length > maxLength ? `${value.slice(0, maxLength)}...` : value;
}

function uniqueStrings(values: Array<string | undefined>): string[] {
  const seen = new Set<string>();
  const result: string[] = [];
  for (const value of values) {
    if (!value || seen.has(value)) {
      continue;
    }
    seen.add(value);
    result.push(value);
  }
  return result;
}

function extractTextNodes(xml: string, tags: string[]): string[] {
  const pattern = tags.map(escapeRegExp).join("|");
  const regex = new RegExp(
    `<(${pattern})(?:\\s[^>]*)?>([\\s\\S]*?)<\\/\\1>`,
    "g"
  );
  const values: string[] = [];
  let match: RegExpExecArray | null;
  while ((match = regex.exec(xml))) {
    values.push(decodeXmlEntities(stripXmlTags(match[2])));
  }
  return values;
}

function extractFirstTagValue(xml: string, tag: string): string {
  const safeTag = escapeRegExp(tag);
  const match = new RegExp(
    `<${safeTag}(?:\\s[^>]*)?>([\\s\\S]*?)<\\/${safeTag}>`
  ).exec(xml);
  return match ? decodeXmlEntities(stripXmlTags(match[1])) : "";
}

function getXmlAttribute(xml: string, name: string): string | undefined {
  const safeName = escapeRegExp(name);
  const match = new RegExp(`(?:^|\\s)${safeName}=(?:"([^"]*)"|'([^']*)')`).exec(
    xml
  );
  const value = match?.[1] ?? match?.[2];
  return value === undefined ? undefined : decodeXmlEntities(value);
}

function normalizeWorkbookTarget(target: string): string {
  const cleanTarget = target.replace(/^\/+/, "");
  const withRoot = cleanTarget.startsWith("xl/")
    ? cleanTarget
    : `xl/${cleanTarget}`;
  return path.posix.normalize(withRoot).replace(/^\.\//, "");
}

function normalizePreviewText(value: string): string {
  return value.replace(/\s+/g, " ").trim();
}

function stripXmlTags(value: string): string {
  return value.replace(/<[^>]*>/g, "");
}

function decodeXmlEntities(value: string): string {
  return value
    .replace(/&#x([0-9a-f]+);/gi, (_, hex: string) =>
      String.fromCodePoint(Number.parseInt(hex, 16))
    )
    .replace(/&#(\d+);/g, (_, decimal: string) =>
      String.fromCodePoint(Number.parseInt(decimal, 10))
    )
    .replace(/&quot;/g, '"')
    .replace(/&apos;/g, "'")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&amp;/g, "&");
}

function trimTrailingEmptyCells(row: string[]) {
  while (row.length > 0 && !row[row.length - 1]) {
    row.pop();
  }
}

function fillSparseRow(row: string[]): string[] {
  return Array.from({ length: row.length }, (_, index) => row[index] || "");
}

function rowsToMarkdownTable(rows: string[][]): string[] {
  const maxColumns = Math.max(...rows.map((row) => row.length), 0);
  if (rows.length === 0 || maxColumns === 0) {
    return ["No previewable table content."];
  }

  const headers = Array.from({ length: maxColumns }, (_, index) =>
    columnName(index)
  );
  return [
    `| # | ${headers.join(" | ")} |`,
    `| --- | ${headers.map(() => "---").join(" | ")} |`,
    ...rows.map((row, rowIndex) => {
      const cells = headers.map((_, columnIndex) =>
        escapeMarkdownTableCell(row[columnIndex] || "")
      );
      return `| ${rowIndex + 1} | ${cells.join(" | ")} |`;
    }),
  ];
}

function columnName(index: number): string {
  let value = "";
  let current = index;
  do {
    value = String.fromCharCode(65 + (current % 26)) + value;
    current = Math.floor(current / 26) - 1;
  } while (current >= 0);
  return value;
}

function escapeMarkdownTableCell(value: string): string {
  return value.replace(/\|/g, "\\|").replace(/\n/g, "<br>");
}

function columnIndexFromCellRef(ref: string): number {
  const match = /^([A-Z]+)/i.exec(ref);
  if (!match) {
    return 0;
  }
  let index = 0;
  for (const char of match[1].toUpperCase()) {
    index = index * 26 + (char.charCodeAt(0) - 64);
  }
  return Math.max(index - 1, 0);
}

function slideIndex(entryName: string): number {
  return Number(/slide(\d+)\.xml$/i.exec(entryName)?.[1] || "0");
}

function sheetIndex(entryName: string): number {
  return Number(/sheet(\d+)\.xml$/i.exec(entryName)?.[1] || "0");
}

function readUInt16(data: Buffer, offset: number): number {
  if (offset + 2 > data.length) {
    throw new Error("Office 文件包读取越界。");
  }
  return data.readUInt16LE(offset);
}

function readUInt32(data: Buffer, offset: number): number {
  if (offset + 4 > data.length) {
    throw new Error("Office 文件包读取越界。");
  }
  return data.readUInt32LE(offset);
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
